public class SwitchCOndition {

	public static void main(String[] args) {
		int value = 34532;
		
		switch(value) {
		
		default:
			System.out.println("Default");

		case 534:
			System.out.println(value);
			break;
			
		case 3453:
			System.out.println(value);
			break;
		case 346:
			System.out.println("true");
			
	
		
		}
		

	}

}
